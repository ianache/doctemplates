import time

from fastapi.testclient import TestClient

from app.auth.session import decrypt_session, encrypt_session
from app.config import settings


def test_proxy_unauthenticated_request(
    client: tuple[TestClient, callable, list],
) -> None:
    test_client, add_handler, requests_received = client

    # Mock backend response
    add_handler(
        method="GET",
        url_part="api/document-designs",
        status_code=200,
        json_data={"data": []},
    )

    response = test_client.get("/api/document-designs")
    assert response.status_code == 200
    assert response.json() == {"data": []}

    # Verify requests sent to backend
    assert len(requests_received) == 1
    backend_req = requests_received[0]
    assert "authorization" not in backend_req.headers
    assert "cookie" not in backend_req.headers
    # Host header is generated by httpx to match target backend host, not the original testserver host
    assert backend_req.headers.get("host") != "testserver.local"


